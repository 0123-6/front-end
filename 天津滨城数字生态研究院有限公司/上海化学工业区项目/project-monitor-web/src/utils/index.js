export function isNumber(num) {
  return num !== '' && typeof Number(num) === 'number' && !isNaN(Number(num))
}

export function thousands(num) {
  if (!isNumber(num)) return '--'
  var regForm = /(\d{1,3})(?=(\d{3})+(?:$|\.))/g;
  return num.toString().replace(regForm, "$1,");
}

